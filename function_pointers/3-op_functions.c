#include "3-calc.h"

/**
*   op_add - Return the sum of the two numbers
*   @a: number 1
*   @b: number 2
*
*   Return: the sum of the a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
*   op_sub - Return the sub of the two numbers
*   @a: number 1
*   @b: number 2
*
*   Return: the sum of the a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
*   op_mul - Return the mul of the two numbers
*   @a: number 1
*   @b: number 2
*
*   Return: the sum of the a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
*   op_div - Return the div of the two numbers
*   @a: number 1
*   @b: number 2
*
*   Return: the div of the a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
*   op_mod - Return the mod of the two numbers
*   @a: number 1
*   @b: number 2
*
*   Return: the mod of the a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
